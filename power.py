
def calc_power(V, I):
    """Calculates power (P) given voltage (V) and currnet (I)"""
    return V * I

def calc_V_from_power(P, I):
    """Calculates voltage (V) given power (P) and current (I)"""
    return P / I

def calc_I_from_power(P, V):
    """Calculates voltage (V) given power (P) and voltage (V)"""
    return P / V
